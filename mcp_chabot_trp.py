import asyncio
import nest_asyncio
import os
from contextlib import AsyncExitStack
import json
from langchain_core.messages import HumanMessage, AIMessage, ToolMessage

nest_asyncio.apply()
load_dotenv()

# class ToolDefinition(TypedDict):
